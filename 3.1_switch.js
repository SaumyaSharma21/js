const month = 3
switch (month) {
    case 1:
        console.log("january")
        break;
    case 2:
        console.log("february")
        break;
    case 3:
        console.log("march")
        break;
    case 4:
        console.log("april")
        break;
    case 1:
        console.log("may")
        break;

    default:
        console.log("default case match")
        break;
}
// if break is not there it executes all other codes after it except default

